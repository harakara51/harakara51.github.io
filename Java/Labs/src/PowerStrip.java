
public class PowerStrip
{

	private int numOutlets;

	private boolean on;

	// methods
	public int getNumOutlets()
	{
		return numOutlets;
	}

	public boolean isOn()
	{
		return on;
	}

	public void setNumOutlets(int numOutlets)
	{
		this.numOutlets = numOutlets;
	}

	public void setOn(boolean on)
	{
		this.on = on;
	}

}
